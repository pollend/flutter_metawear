class Tuple3<T, U, V> {
    final T first;
    final U second;
    final V third;

    Tuple3(T first, U second, V third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }

    @Override
    public boolean equals(Object o) {
        // Generated by IntelliJ
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Tuple3<?, ?, ?> tuple3 = (Tuple3<?, ?, ?>) o;

        return first != null ? first.equals(tuple3.first) : tuple3.first == null &&
                (second != null ? second.equals(tuple3.second) : tuple3.second == null &&
                        (third != null ? third.equals(tuple3.third) : tuple3.third == null));

    }

    @Override
    public int hashCode() {
        // Generated by IntelliJ
        int result = first != null ? first.hashCode() : 0;
        result = 31 * result + (second != null ? second.hashCode() : 0);
        result = 31 * result + (third != null ? third.hashCode() : 0);
        return result;
    }
}
