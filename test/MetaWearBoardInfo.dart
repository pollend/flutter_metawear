    static final List<Type> DEFAULT_MODULES = [
            Logging, DataProcessor, Timer, Macro
    ];
    Map<int, Uint8List> createModuleResponses(List<Type>  moduleClasses) {
      Map<int, Uint8List> responses = Map();
      bool hasDebug = false;

      if (moduleClasses != null) {
        for (Type clazz in moduleClasses) {
          if (MODULE_RESPONSE.containsKey(clazz)) {
            Uint8List response = MODULE_RESPONSE[clazz];
            responses[response[0]] = Uint8List.fromList(response);
          }
          hasDebug |= clazz == Debug;
        }
      }
      for (Type clazz in DEFAULT_MODULES) {
        Uint8List response = MODULE_RESPONSE[clazz];
        responses[response[0]] = Uint8List.fromList(response);
      }

      for (int i = 1; i <= 0x19; i++) {
        if (i == 0xa) {
          responses[0xa] = Uint8List.fromList([0x0a, 0x80, 0x00, 0x00, 0x1C]);
        }
        if (!responses.containsKey(i)) {
          responses[i] = Uint8List.fromList([i, 0x80]);
        }
      }

      if (!hasDebug) {
        responses[0xfe] = Uint8List.fromList([0xfe, 0x80]);
      }

      return responses;
    }


    final Uint8List modelNumber, hardwareRevision, manufacturer, serialNumber;
    final Map<int, Uint8List> moduleResponses = Map();
    final Model model;

    MetaWearBoardInfo(this.model, String modelNumber, String hardwareRevision,[List<Uint8List> moduleResponsesArray]) :
        this.modelNumber = Utf8Encoder().convert(modelNumber),
        this.hardwareRevision = Utf8Encoder().convert(hardwareRevision),
          this.manufacturer = Uint8List.fromList([0x30, 0x30, 0x33, 0x42, 0x46, 0x39]),
          this.serialNumber = Uint8List.fromList([0x4d, 0x62, 0x69, 0x65, 0x6e, 0x74, 0x4c, 0x61, 0x62, 0x20, 0x49, 0x6e, 0x63]) {
      if(moduleResponsesArray != null) {
        for (Uint8List response in moduleResponsesArray) {
          moduleResponses[response[0]] = response;
        }
      }
    }

    factory MetaWearBoardInfo.Modules(List<Type> moduleClasses,[String modelNumber]) {
      MetaWearBoardInfo info = MetaWearBoardInfo(
          null, modelNumber == null ? "deadbeef" : modelNumber, "cafebabe");
      info.moduleResponses.addAll(info.createModuleResponses(moduleClasses));
      return info;
    }


